package automation.lesson1;

// Kelvin and Celsius and Fahrenheit

public class Task1 {
    public static void main(String[] args) {
        int xc = 10;


        double kelvin = 0;
        kelvin = xc + 273.16;
        double zk = kelvin;

        double fahrenheit = 0;
        fahrenheit = (xc * 1.8 + 32);
        double yf = fahrenheit;


        System.out.println(xc);
        System.out.println(zk);
        System.out.println(yf);
    }
}
